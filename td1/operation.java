public class operation {
    public static void main(String[] args) {
        int A,B;
        A=7;
        B=18;
        System.out.println("A="+A+" B="+B);
        A += B; /*A=7 B=18*/ 
        System.out.println("A="+A+" B="+B);
        A -= 8;/* A=25 B=18*/
        System.out.println("A="+A+" B="+B);
        A *= B;/*A=17 B=18 */
        System.out.println("A="+A+" B="+B);
        A /= 2;/*A=306 B=18 */
        System.out.println("A="+A+" B="+B);
        A %= 2;/*A=153 B=18 */
    }
}
